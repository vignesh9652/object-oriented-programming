package com.java.oop.abstraction;

public class Customer {
    int id;
    String name;
    String email;
    String phoneNo;
    String adharNo;



    public Customer(int id, String name, String email, String phoneNo, String adharNo) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phoneNo = phoneNo;
        this.adharNo = adharNo;
    }
}